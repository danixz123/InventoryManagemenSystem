    String allowedCharacters = "0123456789";

    // Create a custom spinner editor that allows only numeric input
    JSpinner.NumberEditor spinnerEditor = new JSpinner.NumberEditor(quantitySpn, "0");
    JFormattedTextField txtQuantity = ((JSpinner.DefaultEditor) spinnerEditor).getTextField();
    txtQuantity.addKeyListener(new KeyAdapter() {
    public void keyTyped(KeyEvent e) {
        char c = e.getKeyChar();
        if (allowedCharacters.indexOf(c) == -1) {
            e.consume(); // Ignore the key press if it's not a numeric character
                Toolkit.getDefaultToolkit().beep(); // Play a beep sound to indicate invalid input
            }
        }
    });
    quantitySpn.setEditor(spinnerEditor);

    
