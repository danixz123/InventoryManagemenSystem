    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
            
            // Create border instances for highlighting input fields in different colors
            Border red = BorderFactory.createLineBorder(Color.red);
            Border normal = BorderFactory.createLineBorder(Color.gray);
            
            // Set user input into username and password variables for further processing
            username = txtUsername.getText();         
            String password = String.valueOf(txtPass.getPassword());
           
            // Check if both username and password match the correct values
            if(username.equals("yourname01") && password.equals("password")){
                 // Display an error message if the fields are empty and highlight them in red
                reportTexT.setText("Fields is empty.");
                txtUsername.setBorder(red);
                txtPass.setBorder(red);      
            }
            
            else if(username.equals("yourname01")){
                reportTexT.setText("Please enter your username");
                txtUsername.setBorder(red);

            }else if(username.isEmpty()){
                reportTexT.setText("Please enter your username");
                txtUsername.setBorder(red);

            }
           else if(username.contains(" ")){
               reportTexT.setText("Seems you accidentally click space!");

           }
           else if(password.equals("password")){
                reportTexT.setText("Please enter your password.");
                txtPass.setBorder(red);
            }else if(password.isEmpty()){
                reportTexT.setText("Please enter your password.");
                txtPass.setBorder(red);
            }
                
                //Display the Dashboard Form
            else{
                txtUsername.setBorder(normal);
                txtPass.setBorder(normal);
                boolean isValid = QueryMethodHandler.loginCheckUser(username,password);
                reportTexT.setText("");
                counter+= 1;
                if(isValid){
                              
                    reportTexT.setText("");  

                    setVisible(false);              
                    counter = 0;
                }              
                else{
                 ///  reportTexT.setText("Invalid user or password");
                   JOptionPane.showMessageDialog(reportTexT,"Invalid user or password(Attempt/s:"+counter+"/3)","Login Denied",JOptionPane.ERROR_MESSAGE);
                   txtUsername.setText("");
                   txtPass.setText("");
                   
                   
                    if(counter==3){                                         
                        try{
                             //program will stop for a minute
        
                            JOptionPane.showMessageDialog(null,"You reached maximum attempts,try again in 1minute!","WARNING",JOptionPane.ERROR_MESSAGE);
                            startCountdown();
                            // reset the counter
                            counter = 0;
                        }
                        catch(Exception e){
                            System.out.println("Interrupted");
                        }

                    }else{
                       // reportTexT.setText("Invalid user or password");
                        

                    }
                }
            }
       
                    
                   
               
            
        
    }//GEN-LAST:event_loginBtnActionPerformed

