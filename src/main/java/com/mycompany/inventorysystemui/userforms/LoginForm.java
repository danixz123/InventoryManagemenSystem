   public boolean addProducts( String product_name,String product_category,Integer product_quantity,String total_price,Integer id,Integer product_price) {
    //   String name = txtUsername.getText();
     
       try (Connection conn = DatabaseConnector.getConnection()) {
            String getUserIdQuery = "SELECT id FROM accounts WHERE username = ?";
            PreparedStatement getUserIdStmt = conn.prepareStatement(getUserIdQuery);
        
            ResultSet rs = getUserIdStmt.executeQuery();
            
            if (rs.next()) {
             //   int id = rs.getInt("id");
               String insertQuery = "INSERT INTO e_table (product_name, product_category, product_quantity, total_price, id, product_price) VALUES (?, ?, ?, ?, ?, ?)";
               getUserIdStmt = conn.prepareStatement(insertQuery);            
                getUserIdStmt.setString(1, product_name);
                getUserIdStmt.setString(2, product_category);
                getUserIdStmt.setInt(3, product_quantity);
                getUserIdStmt.setString(4, total_price);
                getUserIdStmt.setInt(5, id);
                getUserIdStmt.setInt(6, product_price);
                getUserIdStmt.executeUpdate();
               

                return true;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    
     
     }
     private void startCountdown() {
        disableForm();


        count = 59; // Starting number

        javax.swing.Timer timer = new javax.swing.Timer(1000, e -> {

            lblRemaining.setVisible(true);
           
            lblRemain.setVisible(true);
            lblRemain.setText("Remaining: ");
           
            lblRemaining.setText(Integer.toString(count));
            count--;

            if (count < 0) {
                ((javax.swing.Timer) e.getSource()).stop();
                lblRemaining.setVisible(false);
                lblRemain.setVisible(false);
                lblRemaining.setText(null);
                enableForm();

                // Perform any desired action after the countdown finishes
            }
        });
        timer.start();
    }
